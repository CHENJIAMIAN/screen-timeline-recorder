function detectLanguage() {
  const browser = String(navigator.language || "").toLowerCase();
  return browser.startsWith("zh") ? "zh" : "en";
}

function resolveLanguage(preference) {
  if (preference === "en" || preference === "zh") return preference;
  return detectLanguage();
}

function translate(language, key, vars = {}) {
  const table = messages[language] || messages.en;
  const raw = table[key] || messages.en[key] || key;
  return raw.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export { resolveLanguage, translate };
